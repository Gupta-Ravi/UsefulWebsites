package com.useful_websites;

public class cardClass {
    public String classTitle ;
    public String classDesc ;
    public String classVisit ;
    public String classCategory ;
    public String classShare ;
    public String classNum ;

    public cardClass(String classTitle, String classDesc, String classCategory, String classVisit, String classShare, String classNum) {
        this.classTitle = classTitle;
        this.classDesc = classDesc;
        this.classVisit = classVisit;
        this.classCategory = classCategory;
        this.classShare = classShare;
        this.classNum = classNum;
    }
}
